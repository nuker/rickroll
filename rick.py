selfish = commands.Bot(command_prefix='.', self_bot=True)

@selfish.event
async def on_ready():
    print(dim + yel + "-" * 10)
    print(dim + yel + f"Logged in as: {selfish.user}")
    print(dim + yel + "-" * 10)  

async def load_cogs():
    i = 0
    for file in os.listdir("./cogs"):
        if file.endswith(".py"):
            i += 1
            await selfish.load_extension(f"cogs.{file[:-3]}")
    return i

async def main():
    how_many = await load_cogs()

    if how_many == 0:
        print(dim + red + "No cogs to load")
    else:
        print(banner)
        print(dim + yel + f"Loaded {how_many} cog(s)")
        await selfish.start(toktok)

